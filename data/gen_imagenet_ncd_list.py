    fout_train_unlabel = open('ImageNet_ncd_unlabel_%d.txt' % (cls_num), 'w')

    class_list = os.listdir('/home/sunyiyou/dataset/imagenet/val')

    known_class_list = list(set(class_list) - set(IMAGENET_CLASSES_118))
    known_class_list = np.random.choice(known_class_list, 30, replace=False)
    novel_class_list = IMAGENET_CLASSES_30["A"]

    for i, folder_name in enumerate(known_class_list):
        files = []
        for extension in IMG_EXTENSIONS:
            files.extend(glob.glob(os.path.join('/home/sunyiyou/dataset/ILSVRC-2012/train', folder_name, '*' + extension) ))
        for filename in files:
            fout_train_label.write('%s %d\n'%(filename[41:], i))

    for i, folder_name in enumerate(novel_class_list):
        class_label = i + len(known_class_list)
        files = []
